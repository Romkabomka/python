# Унарные и бинарные операторы 
# Унарные операторы всегда имеют один операнд
#    - my_number
#    + my_number
#    not is_activated

# Унарные операторы 
my_num = 10
print(+my_num) # 10

my_bool = True
print(+my_bool) # 1


my_num = 10
print(not my_num) # False

# Бинарные операторы 
# У бинарных операторов два операнда 
#  a = 5
#  a + b
#  a += 5
#  a == b
#  a and b


# Инфиксная запись
# это когда оператор находиться между операндами 
a = True
a + b
a += 5
a or b
a > b
